const fs = require('fs');
const path = require('path');

const root = path.resolve(__dirname, '..');
const outRoot = path.join(root, 'features');

function read(file) {
  return fs.readFileSync(path.join(root, file), 'utf8');
}

function ensureDir(dir) {
  fs.mkdirSync(dir, { recursive: true });
}

function cleanDir(dir) {
  if (fs.existsSync(dir)) fs.rmSync(dir, { recursive: true, force: true });
  ensureDir(dir);
}

function slug(value) {
  return String(value)
    .replace(/([a-z0-9])([A-Z])/g, '$1-$2')
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '') || 'feature';
}

function escapeRegExp(value) {
  return String(value).replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
}

function findOpeningTag(html, id) {
  const pattern = new RegExp(`<([a-z0-9]+)\\b[^>]*\\bid=["']${escapeRegExp(id)}["'][^>]*>`, 'i');
  const match = pattern.exec(html);
  if (!match) return null;
  return {
    tag: match[1].toLowerCase(),
    index: match.index,
    text: match[0]
  };
}

function extractBalancedElement(html, id) {
  const opening = findOpeningTag(html, id);
  if (!opening) return null;

  const tagPattern = new RegExp(`<\\/?${opening.tag}\\b[^>]*>`, 'gi');
  tagPattern.lastIndex = opening.index;

  let depth = 0;
  let match;
  while ((match = tagPattern.exec(html))) {
    const token = match[0];
    const isClose = /^<\//.test(token);
    const isSelfClosing = /\/>$/.test(token);

    if (!isClose && !isSelfClosing) depth += 1;
    if (isClose) depth -= 1;

    if (depth === 0) {
      return html.slice(opening.index, tagPattern.lastIndex);
    }
  }

  return null;
}

function onclickHandlers(html) {
  const handlers = new Set();
  const regex = /\bonclick=["']([^"']+)["']/g;
  let match;
  while ((match = regex.exec(html))) {
    const call = match[1].trim();
    const name = /^([A-Za-z_$][\w$]*)\s*\(/.exec(call)?.[1];
    if (name) handlers.add(name);
    else handlers.add(call);
  }
  return [...handlers].sort();
}

function domIds(html) {
  const ids = new Set();
  const regex = /\bid=["']([^"']+)["']/g;
  let match;
  while ((match = regex.exec(html))) ids.add(match[1]);
  return [...ids].sort();
}

function writeFeature(page, featureId, html, sourceFile) {
  if (!html) return null;
  const name = slug(featureId);
  const dir = path.join(outRoot, page, name);
  ensureDir(dir);

  const ids = domIds(html);
  const handlers = onclickHandlers(html);
  const header = `<!-- Source: ${sourceFile}#${featureId}. Generated by scripts/generate-feature-modules.js. -->\n`;
  fs.writeFileSync(path.join(dir, `${name}.html`), `${header}${html.trim()}\n`);
  fs.writeFileSync(path.join(dir, `${name}.css`), [
    `/* Feature: ${page}/${name}`,
    `   Source: ${sourceFile}#${featureId}`,
    '   Move styles for this feature here from daawah.css, dawaah.css, or inline page styles as the feature is edited.',
    '*/',
    ''
  ].join('\n'));
  fs.writeFileSync(path.join(dir, `${name}.js`), [
    `// Feature: ${page}/${name}`,
    `// Source: ${sourceFile}#${featureId}`,
    '// This module records the DOM IDs and global handlers owned by the feature.',
    '// Move handler implementations here from the legacy page bundle when editing this feature.',
    '(function registerFeatureModule() {',
    '  window.DawaahFeatureModules = window.DawaahFeatureModules || {};',
    `  window.DawaahFeatureModules[${JSON.stringify(`${page}/${name}`)}] = {`,
    `    source: ${JSON.stringify(`${sourceFile}#${featureId}`)},`,
    `    ids: ${JSON.stringify(ids, null, 4).replace(/\n/g, '\n    ')},`,
    `    handlers: ${JSON.stringify(handlers, null, 4).replace(/\n/g, '\n    ')}`,
    '  };',
    '}());',
    ''
  ].join('\n'));

  return {
    page,
    name,
    id: featureId,
    source: sourceFile,
    html: `features/${page}/${name}/${name}.html`,
    css: `features/${page}/${name}/${name}.css`,
    js: `features/${page}/${name}/${name}.js`,
    ids,
    handlers
  };
}

function collectByRegex(html, regex) {
  const ids = new Set();
  let match;
  while ((match = regex.exec(html))) ids.add(match[1]);
  return [...ids];
}

cleanDir(outRoot);

const manifests = [];
const indexHtml = read('index.html');
const adminHtml = read('admin.html');
const officerHtml = read('officer.html');

const publicFeatureIds = [
  ...collectByRegex(indexHtml, /<(?:section|div)\b[^>]*\bid=["']([^"']+)["'][^>]*\bclass=["'][^"']*(?:landing-section|auth-page|dashboard-container|view-container|modal fade|ai-chat-widget)[^"']*["'][^>]*>/gi),
  'landingPage',
  'loginPage',
  'dashboardPage'
];

const adminFeatureIds = [
  ...collectByRegex(adminHtml, /<div\b[^>]*\bid=["']([^"']+)["'][^>]*\bclass=["'][^"']*(?:admin-content|admin-login-screen|modal fade|ai-chat-widget)[^"']*["'][^>]*>/gi),
  'adminContainer'
];

const officerFeatureIds = [
  ...collectByRegex(officerHtml, /<(?:section|div)\b[^>]*\bid=["']([^"']+)["'][^>]*\bclass=["'][^"']*(?:officer-card|tab-pane|ai-chat-widget|modal fade)[^"']*["'][^>]*>/gi)
];

for (const id of [...new Set(publicFeatureIds)]) {
  const feature = writeFeature('public', id, extractBalancedElement(indexHtml, id), 'index.html');
  if (feature) manifests.push(feature);
}

for (const id of [...new Set(adminFeatureIds)]) {
  const feature = writeFeature('admin', id, extractBalancedElement(adminHtml, id), 'admin.html');
  if (feature) manifests.push(feature);
}

for (const id of [...new Set(officerFeatureIds)]) {
  const feature = writeFeature('officer', id, extractBalancedElement(officerHtml, id), 'officer.html');
  if (feature) manifests.push(feature);
}

fs.writeFileSync(path.join(outRoot, 'manifest.json'), `${JSON.stringify({
  generatedAt: new Date().toISOString(),
  note: 'Feature modules generated from the current legacy pages. Keep each feature HTML/CSS/JS together here as the app is modularized.',
  features: manifests
}, null, 2)}\n`);

fs.writeFileSync(path.join(outRoot, 'README.md'), [
  '# Feature Modules',
  '',
  'This folder breaks the large legacy pages into feature-owned folders.',
  '',
  'Each feature folder contains:',
  '',
  '- `*.html` - the feature markup extracted from the current page.',
  '- `*.css` - the feature stylesheet file to receive styles as the feature is edited.',
  '- `*.js` - a fault-map of DOM IDs and global click handlers owned by that feature.',
  '- `runtime/` - ordered JavaScript or CSS slices assembled into the browser bundles.',
  '',
  'The existing `index.html`, `admin.html`, `officer.html`, `daawah.js`, `admin.js`, `officer.js`, and `daawah.css` are still the browser compatibility layer. Edit feature runtime files, then run:',
  '',
  '```powershell',
  'npm.cmd run runtime:assemble',
  '```',
  '',
  '',
  'Regenerate after large HTML changes:',
  '',
  '```powershell',
  'node scripts/generate-feature-modules.js',
  '```',
  ''
].join('\n'));

console.log(`Generated ${manifests.length} feature modules in ${path.relative(root, outRoot)}.`);
